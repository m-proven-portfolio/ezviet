export default function AdminSidebar({ isOpen, onClose }: AdminSidebarProps) {
  const pathname = usePathname();
  const [isExpanded, setIsExpanded] = useState(false);
  const [isPinned, setIsPinned] = useState(false);

  // Load pinned state from localStorage
  useEffect(() => {
    const saved = localStorage.getItem('admin-sidebar-pinned');
    if (saved === 'true') {
      setIsPinned(true);
      setIsExpanded(true);
    }
  }, []);

  const togglePin = () => {
    const newPinned = !isPinned;
    setIsPinned(newPinned);
    setIsExpanded(newPinned);
    localStorage.setItem('admin-sidebar-pinned', String(newPinned));
  };

  const isActive = (href: string) => {
    if (href === '/admin') {
      return pathname === '/admin';
    }
    return pathname.startsWith(href);
  };

  const sidebarWidth = isExpanded || isPinned ? 'w-56' : 'w-16';

  return (
    <>
      {/* Mobile Overlay */}
      {isOpen && (
        <div
          className="fixed inset-0 z-40 bg-black/20 backdrop-blur-sm lg:hidden"
          onClick={onClose}
        />
      )}

      {/* Sidebar */}
      <aside
        className={`fixed top-0 left-0 z-50 h-full border-r border-gray-200 bg-white transition-all duration-200 ease-out ${sidebarWidth} ${isOpen ? 'translate-x-0' : '-translate-x-full lg:translate-x-0'} `}
        onMouseEnter={() => !isPinned && setIsExpanded(true)}
        onMouseLeave={() => !isPinned && setIsExpanded(false)}
      >
        {/* Logo Area */}
        <div className="flex h-16 items-center justify-between border-b border-gray-100 px-4">
          <Link href="/" className="flex items-center gap-2">
            <span className="text-xl font-bold text-emerald-600">EZ</span>
            {(isExpanded || isPinned) && (
              <span className="animate-fade-in text-xl font-bold text-gray-900">Viet</span>
            )}
          </Link>

          {/* Pin Button - Only show when expanded */}
          {(isExpanded || isPinned) && (
            <button
              onClick={togglePin}
              className={`rounded-lg p-1.5 transition-colors ${
                isPinned
                  ? 'bg-emerald-50 text-emerald-600'
                  : 'text-gray-400 hover:bg-gray-100 hover:text-gray-600'
              }`}
              title={isPinned ? 'Unpin sidebar' : 'Pin sidebar'}
            >
              <svg className="h-4 w-4" fill="none" viewBox="0 0 24 24" stroke="currentColor">
                {isPinned ? (
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M5 5a2 2 0 012-2h10a2 2 0 012 2v16l-7-3.5L5 21V5z"
                  />
                ) : (
                  <path
                    strokeLinecap="round"
                    strokeLinejoin="round"
                    strokeWidth={2}
                    d="M5 5a2 2 0 012-2h10a2 2 0 012 2v16l-7-3.5L5 21V5z"
                  />
                )}
              </svg>
            </button>
          )}
        </div>

        {/* Navigation */}
        <nav className="space-y-1 p-3">
          {navItems.map(item => {
            const active = isActive(item.href);
            return (
              <Link
                key={item.href}
                href={item.href}
                onClick={() => onClose()}
                className={`flex items-center gap-3 rounded-lg px-3 py-2.5 transition-all ${
                  active
                    ? 'bg-emerald-50 text-emerald-700'
                    : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                } `}
                title={!isExpanded && !isPinned ? item.label : undefined}
              >
                <span className={active ? 'text-emerald-600' : 'text-gray-400'}>{item.icon}</span>
                {(isExpanded || isPinned) && (
                  <span className="text-sm font-medium whitespace-nowrap">{item.label}</span>
                )}
              </Link>
            );
          })}
        </nav>

        {/* Bottom Section - Back to Site */}
        <div className="absolute right-0 bottom-0 left-0 border-t border-gray-100 p-3">
          <Link
            href="/"
            className="flex items-center gap-3 rounded-lg px-3 py-2.5 text-gray-600 transition-all hover:bg-gray-50 hover:text-gray-900"
            title={!isExpanded && !isPinned ? 'Back to site' : undefined}
          >
            <svg
              className="h-5 w-5 text-gray-400"
              fill="none"
              viewBox="0 0 24 24"
              stroke="currentColor"
            >
              <path
                strokeLinecap="round"
                strokeLinejoin="round"
                strokeWidth={1.5}
                d="M10 19l-7-7m0 0l7-7m-7 7h18"
              />
            </svg>
            {(isExpanded || isPinned) && <span className="text-sm font-medium">Back to site</span>}
          </Link>
        </div>
      </aside>
    </>
  );
}
